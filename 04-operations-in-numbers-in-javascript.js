/* 
prefix and postfix operators.


The ++ operator is overloaded for two types of operands: number and BigInt. 
It first coerces the operand to a numeric value 
and tests the type of it. It performs BigInt increment 
if the operand becomes a BigInt; otherwise, 
it performs number increment.

If used postfix, with operator after 
operand (for example, x++), the increment 
operator increments and returns the value before incrementing.

If used prefix, with operator before 
operand (for example, ++x), the increment operator 
increments and returns the value after incrementing.


*/